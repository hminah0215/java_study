import java.util.Scanner;
class  Sam
{
	public static void main(String[] args) 
	{
		Scanner sc = new Scanner(System.in);
		int a,b,max;

		System.out.println("첫번째 정수를 입력하세요.");
		a = sc.nextInt();
		System.out.println("두번째 정수를 입력하세요.");
		b = sc.nextInt();
		
		max = (a>b)?a:b;
		System.out.println("큰 수는 " + max);		
	}
}
		